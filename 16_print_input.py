# print() -> Mostrar algo na tela

print('Olá Mundo')

# Vaiáveis são espaços de memória utilziados para armazenar uma determinada informação

x = 5

y = 6

soma = x+y

print(f'O resultado da soma de {x} e {y} é {soma}')

# input() -> Recebe alguma informação do usuário e armazena em um espaço de memória

nome = input('Qual seu nome? ')

idade = int(input('Qual sua idade? '))

print(f'{nome} tem {idade} anos.')
